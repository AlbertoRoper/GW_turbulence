    plt.plot(fbrs_min2, T2, color='b', lw=2)
    plt.plot(fbrs_max2, T2, color='b', lw=2)
    if save: plt.savefig('plots/fbr_T_LISA_interp.pdf',
                bbox_inches='tight')

    return T, fbrs_all
